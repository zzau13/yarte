use super::*;

impl Serialize for () {
    #[inline]
    fn to_bytes_mut(&self, buf: &mut BytesMut) {
        render_null(buf)
    }
}

// taken from https://docs.serde.rs/src/serde/ser/impls.rs.html#306

macro_rules! tuple_impls {
    ($($len:expr => ($($n:tt $name:ident)+))+) => {
        $(
            impl<$($name),+> Serialize for ($($name,)+)
            where
                $($name: Serialize,)+
            {
                #[inline]
                fn to_bytes_mut(&self, buf: &mut BytesMut) {
                    begin_array(buf);
                    $(
                        if $n != 0 {
                            write_comma(buf);
                        }
                        self.$n.to_bytes_mut(buf);
                    )+
                    end_array(buf);
                }
            }
        )+
    }
}

